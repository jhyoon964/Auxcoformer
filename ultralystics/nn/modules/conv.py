class cross_AttnBlock(nn.Module):
    def __init__(self, c1, c2, k=3, s=1, d=1):
        super().__init__()
        self.c1 = c1
        channel = int(c1/2)
        self.norm = Normalize(in_channels= channel)
        self.q = torch.nn.Conv2d(channel,
                                 channel,
                                 kernel_size=1,
                                 stride=1,
                                 padding=0)
        self.k = torch.nn.Conv2d(channel,
                                 channel,
                                 kernel_size=1,
                                 stride=1,
                                 padding=0)
        self.v = torch.nn.Conv2d(channel,
                                 channel,
                                 kernel_size=1,
                                 stride=1,
                                 padding=0)
        self.proj_out = torch.nn.Conv2d(channel,
                                        channel,
                                        kernel_size=1,
                                        stride=1,
                                        padding=0)

    def forward(self, xy):
        x, y = torch.split(xy, int(xy.shape[1]/2), dim=1)
        h_ = x
        h_ = self.norm(h_)
        y = self.norm(y)
        q = self.q(y)
        k = self.k(h_)
        v = self.v(h_)

        # compute attention
        b, c, h, w = q.shape
        q = q.reshape(b, c, h*w)
        q = q.permute(0, 2, 1)   # b,hw,c
        k = k.reshape(b, c, h*w)  # b,c,hw
        w_ = torch.bmm(q, k)     # b,hw,hw    w[b,i,j]=sum_c q[b,i,c]k[b,c,j]
        w_ = w_ * (int(c)**(-0.5))
        w_ = torch.nn.functional.softmax(w_, dim=2) # 1 5400 5400

        # attend to values
        v = v.reshape(b, c, h*w)
        w_ = w_.permute(0, 2, 1)   # b,hw,hw (first hw of k, second of q)
        # b, c,hw (hw of q) h_[b,c,j] = sum_i v[b,c,i] w_[b,i,j]
        h_ = torch.bmm(v, w_)
        h_ = h_.reshape(b, c, h, w)

        h_ = self.proj_out(h_)

        return x+h_

